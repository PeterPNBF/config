package com.tydic.config.spi;

import java.util.List;
import java.util.Map;

public interface AutoLoadConfig {

	public abstract Map<String, List<String>> getConfigMaps();

	public abstract void setConfigMaps(Map<String, List<String>> configMaps);

	public abstract String getDirectory();

	public abstract void setDirectory(String directory);

	public abstract String[] getNames();

	public abstract void setNames(String[] names);

	public abstract String[] getPaths();

	public abstract void setPaths(String[] paths);

}